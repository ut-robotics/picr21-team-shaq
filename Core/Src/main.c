volatile uint8_t isCommandReceived = 0; // (5)

uint16_t nSleepPWMValues[2] = {4800, 0};

void CDC_On_Receive(uint8_t* buffer, uint32_t* length) { // (6)
  if (*length == sizeof(Command)) { // (7)
    memcpy(&command, buffer, sizeof(Command)); // (8)

    if (command.delimiter == 0xAAAA) { // (9)
      isCommandReceived = 1;
    }

  }
}

void enableMotorDrivers() {
	HAL_TIM_PWM_Start_DMA(&htim16, TIM_CHANNEL_1, nSleepPWMValues, 2);
}

int32_t clamp_value(int32_t val, int32_t min_val, int32_t max_val){

	if(val < min_val){

		return min_val;
	}
	if(val > max_val){

		return val;
	}
	return val;
}

int32_t controlMotor(MotorControl* motorControl, int16_t position, int16_t set_speed) {

	motorControl->positionChange = position - motorControl->positionPrev;
	motorControl->positionPrev = position;

	// PI controller implementation
	int16_t error = set_speed - motorControl->positionChange;
	motorControl->integral += error;

	motorControl->integral = clamp_value(motorControl->integral, -70000/motorControl->gainI, 70000/motorControl->gainI);

	return error * motorControl->gainP + motorControl->integral * motorControl->gainI;

}


void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
  // Motor control calculations is be called from here

	int32_t motor1PWM = clamp_value(controlMotor(&motor1Control, (int16_t)TIM1->CNT, command.speed1), -65535, 65535);
	int32_t motor2PWM = clamp_value(controlMotor(&motor2Control, (int16_t)TIM3->CNT, command.speed2), -65535, 65535);
	int32_t motor3PWM = clamp_value(controlMotor(&motor3Control, (int16_t)TIM4->CNT, command.speed3), -65535, 65535);



	if(motor1PWM < 0){
		//HAL_GPIO_WritePin(M1_Direction_GPIO_Port, M1_Direction_Pin, 0);
		HAL_GPIO_WritePin(M3_Direction_GPIO_Port, M3_Direction_Pin, 0);
		//TIM2->CCR4 = -motor1PWM; // Timer 2, channel 4
		TIM2->CCR1 = -motor1PWM; // Timer 2, channel 1
	}
	else{
		//HAL_GPIO_WritePin(M1_Direction_GPIO_Port, M1_Direction_Pin, 1);
		HAL_GPIO_WritePin(M3_Direction_GPIO_Port, M3_Direction_Pin, 1);
		//TIM2->CCR4 = motor1PWM; // Timer 2, channel 4
		TIM2->CCR1 = motor1PWM; // Timer 2, channel 1
	}


	if(motor2PWM < 0){
			HAL_GPIO_WritePin(M2_Direction_GPIO_Port, M2_Direction_Pin, 0);
			TIM2->CCR2 = -motor2PWM; // Timer 2, channel 2
		}
		else{
			HAL_GPIO_WritePin(M2_Direction_GPIO_Port, M2_Direction_Pin, 1);
			TIM2->CCR2 = motor2PWM; // Timer 2, channel 2
		}


	if(motor3PWM < 0){
			//HAL_GPIO_WritePin(M3_Direction_GPIO_Port, M3_Direction_Pin, 0);
			HAL_GPIO_WritePin(M1_Direction_GPIO_Port, M1_Direction_Pin, 0);
			//TIM2->CCR1 = -motor3PWM; // Timer 2, channel 1
			TIM2->CCR4 = -motor3PWM; // Timer 2, channel 4
		}
		else{
			//HAL_GPIO_WritePin(M3_Direction_GPIO_Port, M3_Direction_Pin, 1);
			HAL_GPIO_WritePin(M1_Direction_GPIO_Port, M1_Direction_Pin, 1);
			//TIM2->CCR1 = motor3PWM; // Timer 2, channel 1
			TIM2->CCR4 = motor3PWM; // Timer 2, channel 4
		}
}


